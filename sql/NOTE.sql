1단계: 재료 창고 열기 (FROM & JOIN / ON)
언제 써?: "어떤 재료(테이블)가 필요하지?" 결정할 때.

쓰임:

FROM: 메인 재료를 꺼내요.

JOIN: 다른 창고에 있는 재료를 가져와서 붙여요.

ON: 두 재료를 붙일 때 쓰는 **'연결 자석'**이에요. (예: 배우번호=출연명단번호)

2단계: 나쁜 재료 버리기 (WHERE)
언제 써?: "상한 재료나 필요 없는 건 빼야지!" 할 때.

쓰임: 볶기 전에 미리 거르는 거예요. (예: 이름이 'PENELOPE'인 데이터만 남기고 다 버려!)

3단계: 재료끼리 뭉치기 (GROUP BY)
언제 써?: "등급별로 모으고, 카테고리별로 뭉쳐야지!" 할 때.

쓰임: 낱개 데이터를 뭉텅이로 만들어요. 이때부터 개수(COUNT)나 평균(AVG)을 잴 수 있어요.

4단계: 뭉친 것 중에 고르기 (HAVING)
언제 써?: "뭉쳐놨더니 양이 너무 적은 뭉텅이는 버려!" 할 때.

쓰임: GROUP BY로 만든 뭉치들에 조건을 걸 때 써요.

5단계: 예쁘게 담기 (SELECT)
언제 써?: "이제 손님한테 보여줄 부분만 골라내자!" 할 때.

쓰임: 제목, 이름 등 보고 싶은 '칼럼'만 쏙쏙 뽑아요.

6단계: 순서대로 줄 세우기 (ORDER BY)
언제 써?: "키 큰 순서대로, 혹은 이름순으로 정렬하자!" 할 때.

쓰임: 가장 마지막에 합니다. (예: 영화 제목순으로 정렬!)