# 2. 메뉴별로 숫자를 세어주는 계산기 주머니
menu_counts = {}

# 3. 명단을 하나씩 보면서 같은 메뉴끼리 더하기
for item in orders:
    menu_name = item["메뉴"]
    count = item["수량"]

    # 주머니에 이미 이 메뉴가 있으면 숫자를 더하고, 없으면 0개부터 시작해서 더하기
    menu_counts[menu_name] = menu_counts.get(menu_name, 0) + count

# 4. 터미널 화면에 결과 출력하기 (가장 중요!)
